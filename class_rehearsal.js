class Mavericks {
    constructor(name, age, size) {
        this.name = name;
        this.age = age;
        this.size = size;
    }

    getName() {
        return this.name;
    }

    setName(name) {
        this.name = name;
    }

    getAge() {
        return this.age;
    }

    setAge(age) {
        this.age = age;
    }

    getSize() {
        return this.size;
    }

    setSize(size) {
        this.size = size;
    }
}